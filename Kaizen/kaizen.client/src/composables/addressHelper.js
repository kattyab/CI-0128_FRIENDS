function getProvinces() {
  return Object.entries(data.provincias).map(([id, provincia]) => ({
    id: id,
    nombre: provincia.nombre,
  }));
}

function getCantons(provinceId) {
  const province = data.provincias[provinceId];
  if (!province) return [];
  return Object.entries(province.cantones).map(([id, canton]) => ({
    id: id,
    nombre: canton.nombre,
  }));
}

function getDistricts(provinceId, cantonId) {
  const province = data.provincias[provinceId];
  if (!province) return [];
  const canton = province.cantones[cantonId];
  if (!canton) return [];
  return Object.entries(canton.distritos).map(([id, nombre]) => ({
    id: id,
    nombre: nombre,
  }));
}

function getProvinceIdByName(name) {
  const provinces = getProvinces();
  const province = provinces.find((p) => p.nombre.toLowerCase() === name.toLowerCase());
  return province ? province.id : null;
}

